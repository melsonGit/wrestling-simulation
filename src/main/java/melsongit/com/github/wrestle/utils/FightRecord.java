package melsongit.com.github.wrestle.utils;

public class FightRecord {

    private int wins = 0;
    private int losses = 0;

    public int getWins() {
        return wins;
    }
    public void setWins(int wins) {
        this.wins = wins;
    }
    public int getLosses() {
        return losses;
    }
    public void setLosses(int losses) {
        this.losses = losses;
    }
}